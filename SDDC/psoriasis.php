                <h2>Symptoms</h2>
                <p>Melanocytic nevi manifest as small, pigmented spots on the skin, typically brown or black. These moles can vary
                    in size and shape. Regular monitoring is crucial, and any changes in size, color, shape, or texture should be
                    examined by a dermatologist, as they may indicate skin cancer.</p>

                
                <h2>Reasons</h2>
                <p>Genetic Predisposition: Psoriasis has a hereditary component, with a tendency to run in families. Specific genes 
                    associated with the immune system play a role, indicating a genetic predisposition.</p>
                <p>Immune System Dysfunction: Psoriasis is classified as an autoimmune condition where the immune system mistakenly 
                    targets healthy skin cells. This leads to an abnormal inflammatory response and an accelerated turnover of skin cells.</p>
                <p>Environmental Triggers: External factors like stress, infections, and certain medications can trigger or worsen psoriasis symptoms, 
                    particularly in individuals with a genetic predisposition. The interaction between genetic susceptibility and environmental triggers 
                    contributes to the onset or exacerbation of psoriasis.</p>

                <h2>Prevention</h2>
                <p>While there's no known way to prevent psoriasis, certain lifestyle measures can help manage symptoms and reduce flare-ups:</p>
                <ul>
                    <li><strong>Maintain a Healthy Lifestyle:</strong> A healthy diet, regular exercise, and stress management can 
                        contribute to overall well-being and potentially help manage psoriasis.</li>
                    <li><strong>Avoid Triggers:</strong> Identify and avoid factors that trigger or worsen psoriasis symptoms. 
                        Common triggers include stress, certain medications, infections, and skin injuries.</li>
                    <li><strong> Moisturize: </strong> Regularly moisturize the skin to prevent dryness and reduce the risk of flare-ups.</li>
                    <li><strong>Sun Exposure: </strong> Limited exposure to natural sunlight can be beneficial for some individuals with psoriasis. 
                        However, it's crucial to protect the skin from sunburn.</li>
                    <li><strong>Quit Smoking and Limit Alcohol: </strong> Smoking and excessive alcohol consumption have been 
                        linked to an increased risk of psoriasis and may exacerbate symptoms. Quitting smoking and moderating alcohol intake may be beneficial.</li>
                </ul>

